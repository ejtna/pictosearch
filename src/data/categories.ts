// This file is a workaround to translate categories and tags. The following
// scripts are helping to extract all category keys from the private Arasaac
// API and make them available to the translation system of this application.
//
// To update the data the followings steps have to been executed e.g. in a js console
// 1. get data from https://privateapi.arasaac.org/api/categories/de
// 2. get all keys and text from the data field with filterKeysAndText
// 3. get all category translation keys with printTranslationKeys and set them as dummy array
// 4. get all translations for locales/de/translation.json with printTranslations

export function filterKeysAndText(data: any): any {
    const keys = Object.keys(data);
    let translations: any = {};

    keys.forEach(key => {
        if (data[key].text) {
            translations[key] = data[key].text;
        }

        if (data[key].children) {
            translations = {
                ...translations,
                ...filterKeysAndText(data[key].children),
            };
        }
    });

    return translations;
}

export function printTranslationKeys(keys: any): void {
    console.log(Object.keys(keys).map(key => `t('category.${key.replaceAll(' ', '-')}'),`).join('\n'));
}

export function printTranslations(keys:any): void {
    console.log(Object.keys(keys).map(key => `"${key.replaceAll(' ', '-')}": "${keys[key]}"`).join(',\n'));
}

